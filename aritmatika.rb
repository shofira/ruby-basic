hasilPengurangan = bilanganSatu - bilanganDua

puts hasilPengurangan

puts "=================="

hasilPerkalian = bilanganSatu * bilanganDua

puts hasilPerkalian

puts "=================="

hasilPembagian = bilanganSatu / bilanganDua

puts hasilPembagian

puts "=================="

hasilPerpangkatan = bilanganSatu ** bilanganDua

puts hasilPerpangkatan

